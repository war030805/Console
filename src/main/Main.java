package main;

import Console.Console;

public class Main {
    public static void main(String[] args) {
        Console.println("/-YELLOW-/Hello World!");
        System.out.println("as you can see if you type /-YELLOW-/ everything after it is yellow if you do it on the console");
        Console.println("/-GREEN-/this is green");
        Console.println("/-BLUE-/this is blue");
        Console.println("/-RED-/this is red");
        Console.println("you can also make the /-GREEN_B-/background a different color");
        Console.println("or combine the background color and the text color /-GREEN_B BLACK-/background");
    }
}